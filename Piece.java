
public class Piece{
	
	private String name;
	private String description;
	
	public Piece(String name, String description)
	{
		this.name = name;
		this.description = description;
	}
	
	public String GetName()
	{
		return name;
	}
	
	public String GetDescription()
	{
		return description;
	}	
}
